// Why JavaScript is Dynamically Typed Programming language

let age = 35;
console.log(typeof(age));
// Here the 'typeof' method will print the data type of the variable.

age = "Thirty Five";
console.log(typeof(age));

/* 
in Javascript after defining a variable with one specific data type , we can re initialise 
the same variable with another data type , this type of nature is know as 
Dynamically Typed programing.
 */
